// 기본 출력 예제
console.log("Hello world");
console.log("hello world");
console.log(`hello ${"world"}`);

// 문자열 연산 및 이스케이프
console.log("안녕하세요");
console.log('this is "string"');
console.log('this is "string" ');
console.log("this is \n  string ");
console.log("this is \t string");
console.log("this is \\ string");

// 문자열 연산
console.log("hello" + " " + "javascript");
console.log("hello"[1]); // 문자열 인덱싱

// 템플릿 문자열
console.log(`52 + 45 = ${52 + 45}`);

// 숫자 연산
console.log(23);
console.log(23 + 45);
console.log(23 * 45);
console.log(2 + 3 * 5);
console.log(10 % 5);
console.log(10 / 5);

// 비교 연산자
console.log(52 > 52);
console.log(45 == 52);
console.log(45 != 52);
console.log(52 >= 52);

// 논리 연산자
console.log(52 >= 52 && 52 > 52);
console.log(52 >= 52 || 52 > 52);
